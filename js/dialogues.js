/**
 * 대사 세트 선택 + 치환 변수 적용
 *
 * 치환 변수:
 * {이름} - 사용자 이름
 * {키워드} - 대표 키워드
 * {n} - 느낌표 수
 * {셀프유형} - 셀프체크 유형명
 * {인용1}, {인용2}, {인용3} - 실제 사용자 문장
 * {글자수} - 전체 글자 수
 * {문장수} - 전체 문장 수
 * {평균문장길이} - 평균 문장 길이
 * {감정단어수} - 감정어 매칭 수
 * {논리단어수} - 논리어 매칭 수
 * {가장긴문장} - 가장 긴 문장
 * {물음표수} - 물음표 수
 */
function getDialogueSet(analysisResult, userName) {
  const { finalType, gapLevel, mainKeyword, textStats, selfTypeInfo, deepPatterns } = analysisResult;

  const typeKey = finalType;
  const dialogueData = DIALOGUES[typeKey];

  if (!dialogueData) {
    console.error('No dialogue data for type:', typeKey);
    return null;
  }

  const gapSets = dialogueData[gapLevel];
  if (!gapSets || gapSets.length === 0) {
    console.error('No dialogue sets for gap level:', gapLevel);
    return null;
  }

  const setIndex = Math.floor(Math.random() * gapSets.length);
  const selectedSet = gapSets[setIndex];

  // deepPatterns가 있으면 새 변수들 준비, 없으면 폴백
  const dp = deepPatterns || {};
  const quotes = dp.topQuotes || ['', '', ''];

  const processedLines = selectedSet.lines.map(line => ({
    ...line,
    text: line.text
      // 기존 변수
      .replace(/\{이름\}/g, userName)
      .replace(/\{키워드\}/g, mainKeyword)
      .replace(/\{n\}/g, String(textStats.exclamationCount || 0))
      .replace(/\{셀프유형\}/g, selfTypeInfo ? selfTypeInfo.name : '균형 타입')
      // 새 변수: 인용문
      .replace(/\{인용1\}/g, quotes[0] || '')
      .replace(/\{인용2\}/g, quotes[1] || '')
      .replace(/\{인용3\}/g, quotes[2] || '')
      // 새 변수: 통계
      .replace(/\{글자수\}/g, String(dp.charCount || textStats.charCount || 0))
      .replace(/\{문장수\}/g, String(dp.sentenceCount || textStats.sentenceCount || 0))
      .replace(/\{평균문장길이\}/g, String(dp.avgSentenceLength || 0))
      .replace(/\{감정단어수\}/g, String(dp.emotionCount || 0))
      .replace(/\{논리단어수\}/g, String(dp.logicCount || 0))
      .replace(/\{가장긴문장\}/g, dp.longestSentence || '')
      .replace(/\{물음표수\}/g, String(dp.questionCount || textStats.questionCount || 0))
  }));

  return { lines: processedLines };
}
