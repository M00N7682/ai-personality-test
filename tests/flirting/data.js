window.getResult = function (scores) {
  var total = scores.flirt || 0;

  var grade, result;
  if (total >= 72) {
    grade = 'SS';
  } else if (total >= 60) {
    grade = 'S';
  } else if (total >= 48) {
    grade = 'A';
  } else if (total >= 36) {
    grade = 'B';
  } else if (total >= 24) {
    grade = 'C';
  } else if (total >= 12) {
    grade = 'D';
  } else {
    grade = 'F';
  }

  result = window.RESULTS[grade];
  return {
    key: grade,
    name: result.name,
    emoji: result.emoji,
    description: result.description + '\n총점: ' + total + ' / 72점',
    detail: result.detail,
    traits: result.traits,
    strengths: result.strengths,
    weaknesses: result.weaknesses,
    good: result.good,
    bad: result.bad,
  };
};
